// Switch case in js

let day="Monday";

switch(day){
    case "Monday":
    console.log("This is Monday");
    break;

    case "Tuesday":
    console.log("This is Tuesday");
    break;
    
    case "Wednesday":
    console.log("This is Wednesday");
    break;
    
    case "Thursday":
    console.log("This is Thursday");
    break;
    
    case "Friday":
    console.log("This is Friday");
    break;
    
    case "Saturday":
    console.log("This is Saturday");
    break;
    
    case "Sunday":
    console.log("This is Sunday");
    break;
    
}